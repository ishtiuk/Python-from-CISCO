
# by default the 'print' function seperates the values with space " " of the function which are given with 'comma',   but we can also set custom seperators by using the 'sep=' argument.

# I have currently knew that the print function can have the "sep" argument, which seperates the string or output with the given value. like dash ("-")

print("Hey", "I'm", "Jhonny", sep='-')


print("How are you?", end=' ')
print("Fine, And you?")





# the print function can use the both "sep" and "end" argument at the time of invocation....

print("My", "name", "is", sep="_", end="*")
print("Monty", "Python.", sep="*", end="*\n")

print("Monty", "Python.", sep="-", end="*\n")




print("Programming","Essentials","in", sep="***", end="...")
