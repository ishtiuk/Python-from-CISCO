
# NOTE: The rfind() method
# The one-, two-, and three-parameter methods named rfind() do nearly the same things as their counterparts (the ones devoid of the r prefix), but start their searches from 
# the end of the string, not the beginning (hence the prefix r, for right).

# Take a look at the example code in the editor and try to predict its output. Run the code to check if you were right.



# Demonstrating the rfind() method:
print("tau tau tau".rfind("ta"))
print("tau tau tau".rfind("ta", 9))
print("tau tau tau".rfind("ta", 3, 9))
