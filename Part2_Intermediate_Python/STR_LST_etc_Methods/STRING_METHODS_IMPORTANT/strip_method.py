
# NOTE: The strip() method
# The strip() method combines the effects caused by rstrip() and lstrip() - it makes a new string eradicating all the leading and trailing whitespaces.


# Demonstrating the strip() method:
print("[" + "   aleph   ".strip() + "]")

# Look at the second example in the editor. This is the result it returns:

# [aleph]
# output

